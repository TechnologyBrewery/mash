package org.technologybrewery.mash.objects;

/**
 * A generic POJO object for test use with the XlsxToTransferObjectMediator.
 */
public class GenericObject {

    public String name;
    public String id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
